
CREATE TABLE IF NOT EXISTS "Images" (
	"id" INTEGER NOT NULL UNIQUE,
	"filepath" TEXT,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "NotificationPreferences" (
	"id" INTEGER NOT NULL UNIQUE,
	"email" BOOLEAN,
	"sms" BOOLEAN,
	"pushNotification" BOOLEAN,
	PRIMARY KEY("id")
);

CREATE TABLE IF NOT EXISTS "Users" (
	"id" INTEGER NOT NULL UNIQUE,
	"username" TEXT NOT NULL UNIQUE,
	"passwordHash" TEXT NOT NULL,
	"passwordSalt" TEXT NOT NULL,
	"email" TEXT UNIQUE,
	"profile_image_id" INTEGER,
	"birthday_month" INTEGER,
	"birthday_day" INTEGER,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("profile_image_id") REFERENCES "Images"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE IF NOT EXISTS "Friends" (
	"id" INTEGER NOT NULL UNIQUE,
	"name" TEXT NOT NULL,
	"birthday_month" INTEGER,
	"birthday_day" INTEGER,
	"profile_image_id" INTEGER,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("profile_image_id") REFERENCES "Images"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE IF NOT EXISTS "Relationships" (
	"id" INTEGER NOT NULL UNIQUE,
	"user_id" INTEGER NOT NULL,
	"friend_id" INTEGER NOT NULL,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"relationship_tier" INTEGER,
	PRIMARY KEY("id"),
	FOREIGN KEY ("friend_id") REFERENCES "Friends"("id")
	ON UPDATE NO ACTION ON DELETE CASCADE,
	FOREIGN KEY ("user_id") REFERENCES "Users"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE IF NOT EXISTS "Interactions" (
	"id" INTEGER NOT NULL UNIQUE,
	"date" DATETIME NOT NULL,
	"user_id" INTEGER NOT NULL,
	"interaction_type" VARCHAR,
	"location" TEXT,
	"name" TEXT,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("user_id") REFERENCES "Users"("id")
	ON UPDATE NO ACTION ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS "InteractionsAttendees" (
	"id" INTEGER NOT NULL UNIQUE,
	"interaction_id" INTEGER,
	"friend_id" INTEGER,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("interaction_id") REFERENCES "Interactions"("id")
	ON UPDATE NO ACTION ON DELETE CASCADE,
	FOREIGN KEY ("friend_id") REFERENCES "Friends"("id")
	ON UPDATE NO ACTION ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS "UserSettings" (
	"id" INTEGER NOT NULL UNIQUE,
	"user_id" INTEGER,
	"NotificationPreferences_id" INTEGER,
	"ui_theme" VARCHAR,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("user_id") REFERENCES "Users"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION,
	FOREIGN KEY ("NotificationPreferences_id") REFERENCES "NotificationPreferences"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION
);

CREATE TABLE IF NOT EXISTS "Sessions" (
	"id" INTEGER NOT NULL UNIQUE,
	"user_id" INTEGER NOT NULL,
	"session_token" TEXT NOT NULL,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"expires_at" DATETIME,
	"is_revoked" BOOLEAN,
	PRIMARY KEY("id"),
	FOREIGN KEY ("user_id") REFERENCES "Users"("id")
	ON UPDATE NO ACTION ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS "UserFriendUpdates" (
	"id" INTEGER NOT NULL UNIQUE,
	"user_id" INTEGER NOT NULL,
	"friend_id" INTEGER NOT NULL,
	"action" VARCHAR NOT NULL,
	"created_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	"updated_at" DATETIME DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY("id"),
	FOREIGN KEY ("user_id") REFERENCES "Users"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION,
	FOREIGN KEY ("friend_id") REFERENCES "Friends"("id")
	ON UPDATE NO ACTION ON DELETE NO ACTION
);